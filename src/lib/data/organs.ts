export function getOrgan(slug: string): OrganData | undefined {
  return organs.find((o) => o.slug === slug);
}

export function searchOrgans(query: string): OrganData[] {
  const q = query.toLowerCase();
  return organs.filter(
    (o) =>
      o.name.toLowerCase().includes(q) ||
      o.system.toLowerCase().includes(q) ||
      o.description.toLowerCase().includes(q) ||
      o.diseases.some((d) => d.name.toLowerCase().includes(q)) ||
      o.functions.some((f) => f.toLowerCase().includes(q))
  );
}

export function getOrgansBySystem(system: string): OrganData[] {
  return organs.filter((o) => o.system.toLowerCase().includes(system.toLowerCase()));
}

